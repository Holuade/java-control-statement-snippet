package com.learnfrom.bright.controlStructure;

/**
 * Demonstrates the use of do-while loop in Java.
 */
public class DoWhileLoopDemo {

    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println("Do-While Count: " + i);
            i++;
        } while (i <= 5);
    }
}
