package com.learnfrom.bright.controlStructure;

/**
 * Demonstrates the use of while loop in Java.
 */
public class WhileLoopDemo {

    public static void main(String[] args) {
        int i = 1;

        while (i <= 5) {
            System.out.println("While Count: " + i);
            i++;
        }
    }
}
